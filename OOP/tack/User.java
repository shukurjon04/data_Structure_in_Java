package OOP.tack;

public class User {
    String firstname;
    String lastname;
    String phonenumber;
    String email;
    String password;
    User(String firstname,String lastname,String phonenumber,String email, String password){
        this.firstname = firstname;
        this.lastname = lastname;
        this.phonenumber = phonenumber;
        this.email = email;
        this.password = password;
    }
}
